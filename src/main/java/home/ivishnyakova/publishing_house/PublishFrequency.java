package home.ivishnyakova.publishing_house;
/* Перечисление PublishFrequency описывает варианты
* частоты выхода журнала.
* */
public enum PublishFrequency {
    WEEK,       //1 раз в неделю
    TWO_WEEK,   //1 раз в 2 недели
    MONTH,      //1 раз в месяц
    TWO_MONTH,  //1 раз в 2 месяца
    HALF_YEAR,  //1 раз в полгода
    YEAR        //1 раз в год
}
